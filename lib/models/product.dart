class Product {
  final int id;
  final String name;
  final String category;
  final String image;
  final String desciption;
  final double price;
  int quantity;

  Product({
    required this.id,
    required this.name,
    required this.category,
    required this.image,
    required this.desciption,
    required this.price,
    required this.quantity,
  });
}
